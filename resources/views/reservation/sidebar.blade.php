<h4>General Information</h4>
<p>Effective March 27, 2016, electronic prescribing for both controlled and non-controlled substances will be required in New York State.</p>

<p>To allow providers to electronically order prescriptions on hospital computers by the March 27th deadline,
    every ordering provider will need to go through a 10 minute process to verify their identity against a
    government-issued form of identification (e.g., driver’s license) and to register their fingerprint.</p>

<ul>
    <li>Use the calendar to the right to indicate when you anticipate you will be at the hospital to go through the ID Proofing process.  Note that the calendar updates to indicate how many of your colleagues have also picked the same time slot.</li>
    <li>Resources will be available in the 5th floor lobby area Tuesday, January 19 – Friday, January 22* and Monday, January 25 – Thursday, January 28 from 8:00a – 4:00p  to:</li>
    <ul>
        <li>Validate the provider’s identity</li>
        <li>Register their finger print, and</li>
        <li> Configure a “soft token” smart phone app that can be used in lieu of a finger print scan (i.e., for remote access).</li>
    </ul>
    <li>Note that the ID proofing process requires that providers present a valid government-issued form of identification (e.g., driver’s license, passport).</li>
    <li>To expedite the process, providers are encouraged to download the free Symantec VIP Access app onto their iPhone, or android smartphone, from the App Store or Google Play Store before starting the above process.</li>
    <li>*Note that resources will be available January 19 – January 22 to assist you with any questions regarding the Practice Unite secure testing application.</li>
</ul>

